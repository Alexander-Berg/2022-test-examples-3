            // Порядок факторов в слайсе market_category должен совпадать с порядком в enum TCategoryFactors::ECategoryFactors
            ASSERT_EQ(Y_ARRAY_SIZE(CATEGORY_FACTORS_NAMES), TCategoryFactors::COUNT);

            for (size_t i = 0; i < TCategoryFactors::COUNT; i++) {
                const auto nameInEnum = ToString(static_cast<TCategoryFactors::ECategoryFactors>(i));
                const auto nameInSlice = CATEGORY_FACTORS_NAMES[i];
                EXPECT_EQ(nameInEnum, nameInSlice);
            }
