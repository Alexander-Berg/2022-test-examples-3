        TEST(Factors, descriptionPresence) {
            THashSet<TString> genNames(
                BASE_FACTORS_NAMES,
                BASE_FACTORS_NAMES + Y_ARRAY_SIZE(BASE_FACTORS_NAMES)
            );

            // Факторы могут удаляться из енама TBaseFactors но не могут удалятся из factors_gen.in
            // Поэтому в TBaseFactors факторов меньше чем в factors_gen.in (с учетом пока не включенных туда факторов)
            ASSERT_LT(EBaseFactors::COUNT, genNames.size() + NotIncludedInBaseFormulaFactors.size() + 1);

            // Проверяем что фактор из TBaseFactors присутсвтует в factors_gen.in или в списке не включенных факторов
            for (size_t i = 0; i < EBaseFactors::COUNT; i++) {
                const auto factor = static_cast<EBaseFactors::E>(i);
                const auto inGenNames = !NotIncludedInBaseFormulaFactors.contains(factor);
                EXPECT_EQ(genNames.contains(ToString(factor)), inGenNames);
            }
        }

