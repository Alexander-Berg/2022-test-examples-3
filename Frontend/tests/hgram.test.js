const constructHgram = require('../unistat/lib/hgram');

describe('конструктор гистограммных значений', () => {
    it('Кладет значение в левый крайний интервал', () => {
        expect(constructHgram([0], [0, 1, 2])).toEqual([[0, 1], [1, 0], [2, 0]]);
    });

    it('Кладет значение в правый крайний интервал', () => {
        expect(constructHgram([3], [0, 1, 2])).toEqual([[0, 0], [1, 0], [2, 1]]);
    });

    it('Кладет значения в корзины в соответствии с левыми границами', () => {
        expect(constructHgram([3, 10, 0, 0, 1, 2], [0, 1, 3])).toEqual([[0, 2], [1, 2], [3, 2]]);
    });

    it('Корректно завершается, если значений нет', () => {
        expect(constructHgram([], [0, 1, 3])).toEqual([[0, 0], [1, 0], [3, 0]]);
    });

    it('Корректно завершается, если границ нет', () => {
        expect(constructHgram([1, 2, 3], [])).toEqual([]);
    });

    it('Возвращает пустую гистаграмму, если значения не попадают в границы', () => {
        expect(constructHgram([1, 2, 3, -1, -Infinity, NaN], [10, 20])).toEqual([[10, 0], [20, 0]]);
    });
});
