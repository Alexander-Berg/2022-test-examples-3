import { formatSecondsForDuration, padZero, generateTestingHours } from './time';
import { moment } from '../lib/moment';

describe('padZero', () => {
    it('should pad when less 10', () => {
        expect(padZero(9)).toBe('09');
    });

    it('should not pad when greater than 10', () => {
        expect(padZero(15)).toBe('15');
    });
});

describe('formatSecondsForDuration', () => {
    it('should format seconds correctly 1', () => {
        expect(formatSecondsForDuration(59)).toBe('0:59');
    });

    it('should format seconds correctly 2', () => {
        expect(formatSecondsForDuration(21.245)).toBe('0:21');
    });

    it('should format seconds correctly 2', () => {
        expect(formatSecondsForDuration(36.75621)).toBe('0:36');
    });

    it('should format minutes correctly 1', () => {
        expect(formatSecondsForDuration(61)).toBe('1:01');
    });

    it('should format minutes correctly 2', () => {
        expect(formatSecondsForDuration(60)).toBe('1:00');
    });

    it('should format minutes correctly 3', () => {
        expect(formatSecondsForDuration(601)).toBe('10:01');
    });

    it('should format minutes correctly 4', () => {
        expect(formatSecondsForDuration(601)).toBe('10:01');
    });

    it('should format minutes correctly 5', () => {
        expect(formatSecondsForDuration(3599)).toBe('59:59');
    });

    it('should format hours correctly 1', () => {
        expect(formatSecondsForDuration(3600)).toBe('1:00:00');
    });
    it('should format hours correctly 2', () => {
        expect(formatSecondsForDuration(7200 + 30 * 60 + 24)).toBe('2:30:24');
    });
    it('should format hours correctly 3', () => {
        expect(formatSecondsForDuration(3600 * 350 + 1)).toBe('350:00:01');
    });
});

